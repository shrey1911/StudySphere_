exports.deleteCourse = async (req, res) => {
  try {
    const { courseId } = req.body
    console.log("courseId", courseId);
    console.log("userId", req.user); 
    
    // Find the course
    const course = await Course.findById(courseId)
    if (!course) {
      return res.status(404).json({
        success: false,
        message: "Course not found"
      })
    }

    // Verify the instructor
    if (course.instructor.toString() !== req.user.id) {
      return res.status(403).json({
        success: false,
        message: "You are not authorized to delete this course"
      })
    }

    // Delete all section's materials first
    for (const sectionId of course.courseContent) {
      const section = await Section.findById(sectionId)
      if (section?.materials?.length > 0) {
        // Delete materials
        await Section.findByIdAndUpdate(sectionId, {
          $set: { materials: [] }
        })
      }
    }

    // Delete sections and sub-sections
    for (const sectionId of course.courseContent) {
      // Delete sub-sections of the section
      const section = await Section.findById(sectionId)
      if (section) {
        for (const subSectionId of section.subSection) {
          await SubSection.findByIdAndDelete(subSectionId)
        }
      }
      // Delete the section
      await Section.findByIdAndDelete(sectionId)
    }

    // Remove course from students' enrolled courses
    await User.updateMany(
      { courses: courseId },
      { $pull: { courses: courseId } }
    )

    // Delete course thumbnail from cloudinary if exists
    if (course.thumbnail) {
      const thumbnailId = course.thumbnail.split('/').pop().split('.')[0]
      await cloudinary.uploader.destroy(thumbnailId)
    }

    // Finally delete the course
    await Course.findByIdAndDelete(courseId)

    return res.status(200).json({
      success: true,
      message: "Course deleted successfully"
    })
  } catch (error) {
    console.error("Error deleting course:", error)
    return res.status(500).json({
      success: false,
      message: "Failed to delete course",
      error: error.message
    })
  }
}

